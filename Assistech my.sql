CREATE DATABASE Assistech;
USE Assistech;

-- ENTIDADE CLIENTE
CREATE TABLE CLIENTE(
    ID INTEGER NOT NULL AUTO_INCREMENT,
    NOME VARCHAR(100) NOT NULL,
    CPF VARCHAR(14),
    TELEFONE VARCHAR(15) NOT NULL,
    ENDERECO VARCHAR(200),
    EMAIL VARCHAR(50),
    PRIMARY KEY (ID)

);

-- ENTIDADE CELULAR
CREATE TABLE CELULAR(
    ID INTEGER NOT NULL AUTO_INCREMENT,
    MODELO VARCHAR(20) NOT NULL,
    MARCA VARCHAR(20) NOT NULL,
    ANO VARCHAR(4),
    SERIAL VARCHAR(20),
    ESTADO_USO VARCHAR(20),
    IS_PROPRIEDADE VARCHAR(3),
    ID_CLIENTE INTEGER,
    PRIMARY KEY (ID),
    FOREIGN KEY (ID_CLIENTE) REFERENCES CLIENTE(ID)
);

-- ENTIDADE FUNCIONARIO
CREATE TABLE FUNCIONARIO(
    ID INTEGER NOT NULL AUTO_INCREMENT,
    NOME VARCHAR(100) NOT NULL,
    CPF VARCHAR(14) NOT NULL,
    TELEFONE VARCHAR(14) NOT NULL,
    ENDERECO VARCHAR(200) NOT NULL,
    EMAIL VARCHAR(50),
    SALARIO FLOAT NOT NULL,
    DATA_CONTRATACAO VARCHAR(10) NOT NULL,
    DATA_DEMISSAO VARCHAR(10),
    PRIMARY KEY (ID)
);

-- ENTIDADE ORDEM DE SERVICO
CREATE TABLE ORDEM_SERVICO(
    NUMERO_ORDEM INTEGER NOT NULL AUTO_INCREMENT,
    DATA_ENTRADA VARCHAR(10) NOT NULL,
    DATA_SAIDA VARCHAR(10),
    ORCAMENTO FLOAT NOT NULL,
    ALUGUEL VARCHAR(3),
    ID_TECNICO INTEGER,
    ID_APARELHO INTEGER NOT NULL,
    PRIMARY KEY(NUMERO_ORDEM)
);

-- ALUGA (RELACIONAL)
CREATE TABLE CLIENTE_CELULAR(
    ID_CLIENTE INTEGER NOT NULL,
    ID_CELULAR INTEGER NOT NULL,
    DATA_INICIO VARCHAR(10),
    DATA_TERMINO VARCHAR(10),
    FOREIGN KEY (ID_CLIENTE) REFERENCES CLIENTE(ID),
    FOREIGN KEY (ID_CELULAR) REFERENCES CELULAR(ID)
);

-- ENTIDADE CAIXA
CREATE TABLE CAIXA(
    ID_RECIBO INTEGER NOT NULL AUTO_INCREMENT,
    TIPO_SERVICO VARCHAR(50) NOT NULL,
    VALOR_FINAL FLOAT NOT NULL,
    FORMA_PAGAMENTO VARCHAR(20) NOT NULL,
    NRO_ORDEM INTEGER NOT NULL,
    PRIMARY KEY (ID_RECIBO),
    FOREIGN KEY (NRO_ORDEM) REFERENCES ORDEM_SERVICO(NUMERO_ORDEM)
);

-- ENTIDADE FORNECEDOR
CREATE TABLE FORNECEDOR(
    ID INTEGER NOT NULL AUTO_INCREMENT,
    CNPJ VARCHAR(14) NOT NULL,
    NOME VARCHAR(100) NOT NULL,
    TELEFONE VARCHAR(14) NOT NULL,
    DATA_PRIMEIRA_COMPRA VARCHAR(10) NOT NULL,

    PRIMARY KEY (ID)
);

-- ENTIDADE PECAS
CREATE TABLE PECAS(
    ID INTEGER NOT NULL AUTO_INCREMENT,
    NOME VARCHAR(30) NOT NULL,
    MARCA VARCHAR(20) NOT NULL,
    QUANTIDADE INTEGER,
    ID_FORNECEDOR INTEGER NOT NULL,

    PRIMARY KEY (ID),
    FOREIGN KEY (ID_FORNECEDOR) REFERENCES FORNECEDOR(ID)
);

-- ENTIDADE EQUIPAMENTO
CREATE TABLE EQUIPAMENTO(
    ID INTEGER NOT NULL AUTO_INCREMENT,
    NOME VARCHAR(100) NOT NULL,
    MARCA VARCHAR(20) NOT NULL,
    PRECO FLOAT NOT NULL,
    DATA_COMPRA VARCHAR(11) NOT NULL,
    DATA_ULTIMA_MANUTENCAO VARCHAR(11),
    MANUTENCAO VARCHAR(3),

    PRIMARY KEY (ID)
);

